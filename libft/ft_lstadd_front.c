#include "libft.h"

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (*alst == 0)
	{
		*alst = new;
		return ;
	}
	if (*alst != 0 && new != 0)
	{
		new->next = *alst;
		*alst = new;
	}
}
